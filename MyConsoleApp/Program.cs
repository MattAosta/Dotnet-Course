//Character Literal
//Only 1 character
Console.WriteLine('b');

//Integer Literal
Console.WriteLine(123);

//Floating-point Literal
//literal suffic can be both f and F
//least precise, best for fixed fractional values
Console.WriteLine(0.25F);

//Double Literal (default for decimals)
Console.WriteLine(2.625);

//Decimal Literal
//literal suffix can be both m or M
Console.WriteLine(12.39816m);

//Bolean Literal
Console.WriteLine(true);
Console.WriteLine(false);